package com.cooperation.promanager.pojo;

public class ProSkuInfo {
    private Long proskuid;

    private Long baseproid;

    private Long prostdid;

    private String proskuname;

    public Long getProskuid() {
        return proskuid;
    }

    public void setProskuid(Long proskuid) {
        this.proskuid = proskuid;
    }

    public Long getBaseproid() {
        return baseproid;
    }

    public void setBaseproid(Long baseproid) {
        this.baseproid = baseproid;
    }

    public Long getProstdid() {
        return prostdid;
    }

    public void setProstdid(Long prostdid) {
        this.prostdid = prostdid;
    }

    public String getProskuname() {
        return proskuname;
    }

    public void setProskuname(String proskuname) {
        this.proskuname = proskuname;
    }
}